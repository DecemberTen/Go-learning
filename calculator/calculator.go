package calculator

// Add 用于计算两个整数的和。
// 参数：a 是第一个整数；b 是第二个整数。
// 返回值：返回 a 与 b 的和。
func Add(a, b int) int {
	return a + b
}

// Subtract 用于计算两个整数的差。
// 参数：a 是被减数；b 是减数。
// 返回值：返回 a 减去 b 的结果。
func Subtract(a, b int) int {
	return a - b
}

// Multiply 用于计算两个整数的乘积。
// 参数：a 是第一个整数；b 是第二个整数。
// 返回值：返回 a 与 b 的乘积。
func Multiply(a, b int) int {
	return a * b
}
